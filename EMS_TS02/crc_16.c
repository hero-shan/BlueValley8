unsigned short FireC_check_crc16 (const unsigned char *pucData, unsigned short uslen)
{
	unsigned short usCrc = 0;
	
	while (uslen)
	{
	usCrc = gusCrcTable[(usCrc >> 8 ^ *pucData) & 0xFFU] ^ (usCrc << 8);
	++pucData;
	--uslen;
 }
 return (usCrc);
}

uint16_t mc_check_crc16(uint8_t *buf,uint16_t len)
{
	uint8_t index;
	uint16_t check16=0;
	uint8_t crc_low=0XFF;
	uint8_t crc_high=0XFF;
	while(len--)
	{
		index=crc_high^(*buf++);
		crc_high=crc_low^CRC16HiTable[index];
		crc_low=CRC16LoTable[index];
	}
	check16 +=crc_high;
	check16 <<=8;
	check16+=crc_low;
	return check16;
}
